#include "secure_wdt.h"

static volatile funcptr_void secure_wdt_interrupt_callback = 0;

void WDT_Handler(void)
{
	secure_wdt_interrupt_callback();
}

void secure_wdt_interrupt_callback_register(funcptr_void pfunction)
{
	secure_wdt_interrupt_callback = pfunction;
	NVIC_EnableIRQ(WDT_IRQn);
	return;
}

void secure_wdt_init(void)
{
	uint32_t clk_rate;
	uint16_t timeout_period;

	clk_rate       = 1000;
	timeout_period = 4096;
	wdt_set_timeout_period(&WATCHDOG, clk_rate, timeout_period);
	wdt_enable(&WATCHDOG);
}

void secure_wdt_refresh(void)
{
	wdt_feed(&WATCHDOG);
}
